// ─── Helpers ─────────────────────────────────────────────────────────────────

const h1 = (t) => new Paragraph({ text: t, heading: HeadingLevel.HEADING_1, spacing: { before: 480, after: 200 } });
const h2 = (t) => new Paragraph({ text: t, heading: HeadingLevel.HEADING_2, spacing: { before: 300, after: 140 } });
const h3 = (t) => new Paragraph({ text: t, heading: HeadingLevel.HEADING_3, spacing: { before: 220, after: 100 } });

const p  = (t, o = {}) => new Paragraph({ children: [new TextRun({ text: t, ...o })], spacing: { after: 100 } });
const pb = (t) => p(t, { bold: true });
const pi = (t) => p(t, { italics: true, color: "444444" });
const sp = () => new Paragraph({ children: [new TextRun("")], spacing: { after: 80 } });

const quote = (t) => new Paragraph({
  children: [new TextRun({ text: t, italics: true, color: "1A3A5C" })],
  indent: { left: 720 },
  spacing: { before: 100, after: 200 },
  border: { left: { style: BorderStyle.THICK, size: 10, color: "4ECDC4" } },
});

const note = (t) => new Paragraph({
  children: [new TextRun({ text: "✏  " + t, color: "880000", size: 19 })],
  shading: { type: ShadingType.SOLID, color: "FFF2F2" },
  spacing: { before: 80, after: 120 },
  border: {
    left: { style: BorderStyle.THICK, size: 8, color: "CC3333" },
    top: { style: BorderStyle.SINGLE, size: 2, color: "FFBBBB" },
    bottom: { style: BorderStyle.SINGLE, size: 2, color: "FFBBBB" },
    right: { style: BorderStyle.SINGLE, size: 2, color: "FFBBBB" },
  },
});

const doubt = (t) => new Paragraph({
  children: [new TextRun({ text: "⚠  " + t, color: "664400", size: 19 })],
  shading: { type: ShadingType.SOLID, color: "FFFBF0" },
  spacing: { before: 80, after: 120 },
  border: {
    left: { style: BorderStyle.THICK, size: 8, color: "CC8800" },
    top: { style: BorderStyle.SINGLE, size: 2, color: "FFDD88" },
    bottom: { style: BorderStyle.SINGLE, size: 2, color: "FFDD88" },
    right: { style: BorderStyle.SINGLE, size: 2, color: "FFDD88" },
  },
});

const openq = (id, t) => new Paragraph({
  children: [
    new TextRun({ text: `[${id}] `, bold: true, color: "0055AA" }),
    new TextRun({ text: t, color: "0055AA" }),
  ],
  spacing: { after: 80 },
});

const status = (s, upd, qs, log) => {
  const col = s === "LOCKED" ? "1A7A1A" : s === "REVIEW" ? "BB6600" : "CC0000";
  return new Paragraph({
    children: [
      new TextRun({ text: `Status: ${s}`, bold: true, color: col }),
      new TextRun({ text: `  |  Updated: ${upd}  |  Open Qs: ${qs}  |  Log: ${log}` }),
    ],
    shading: { type: ShadingType.SOLID, color: "F7F7F7" },
    spacing: { before: 60, after: 180 },
    border: {
      left: { style: BorderStyle.THICK, size: 10, color: "4ECDC4" },
      top: { style: BorderStyle.SINGLE, size: 2, color: "CCCCCC" },
      bottom: { style: BorderStyle.SINGLE, size: 2, color: "CCCCCC" },
      right: { style: BorderStyle.SINGLE, size: 2, color: "CCCCCC" },
    },
  });
};

const bul = (t, lv = 0) => new Paragraph({ children: [new TextRun({ text: t })], bullet: { level: lv }, spacing: { after: 60 } });
const cod = (t) => new Paragraph({ children: [new TextRun({ text: t, font: "Courier New", size: 18 })], shading: { type: ShadingType.SOLID, color: "F0F0F0" }, indent: { left: 360 }, spacing: { before: 60, after: 60 } });
const pb_ = () => new Paragraph({ children: [new PageBreak()] });

// ─── Table ────────────────────────────────────────────────────────────────────

const pct = (n) => ({ size: n, type: WidthType.PERCENTAGE });

const tbl = (headers, rows, widths) => {
  const hRow = new TableRow({
    tableHeader: true,
    children: headers.map((h, i) => new TableCell({
      children: [new Paragraph({ children: [new TextRun({ text: h, bold: true, color: "FFFFFF", size: 20 })] })],
      shading: { type: ShadingType.SOLID, color: "1A3A5C" },
      width: widths ? pct(widths[i]) : undefined,
      margins: { top: 70, bottom: 70, left: 110, right: 110 },
    })),
  });
  const dRows = rows.map((row, ri) => new TableRow({
    children: row.map((cell, ci) => new TableCell({
      children: [new Paragraph({ children: [new TextRun({ text: String(cell), size: 18 })] })],
      shading: { type: ShadingType.SOLID, color: ri % 2 === 0 ? "FFFFFF" : "F4F8FC" },
      width: widths ? pct(widths[ci]) : undefined,
      margins: { top: 55, bottom: 55, left: 110, right: 110 },
    })),
  }));
  return new Table({ rows: [hRow, ...dRows], width: { size: 100, type: WidthType.PERCENTAGE } });
};

