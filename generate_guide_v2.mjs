// ─── Helpers ─────────────────────────────────────────────────────────────────

function h1(text) {
  return new Paragraph({
    text,
    heading: HeadingLevel.HEADING_1,
    spacing: { before: 400, after: 120 },
  });
}

function h2(text) {
  return new Paragraph({
    text,
    heading: HeadingLevel.HEADING_2,
    spacing: { before: 280, after: 80 },
  });
}

function h3(text) {
  return new Paragraph({
    text,
    heading: HeadingLevel.HEADING_3,
    spacing: { before: 200, after: 60 },
  });
}

function para(text, options = {}) {
  return new Paragraph({
    children: [new TextRun({ text, size: 22, ...options })],
    spacing: { before: 80, after: 80 },
  });
}

function bold(text) {
  return new Paragraph({
    children: [new TextRun({ text, bold: true, size: 22 })],
    spacing: { before: 80, after: 40 },
  });
}

function bullet(text, level = 0) {
  return new Paragraph({
    children: [new TextRun({ text, size: 22 })],
    bullet: { level },
    spacing: { before: 40, after: 40 },
  });
}

function numbered(text, level = 0) {
  return new Paragraph({
    children: [new TextRun({ text, size: 22 })],
    numbering: { reference: "main-numbering", level },
    spacing: { before: 40, after: 40 },
  });
}

function inlineRuns(parts) {
  // parts: array of {text, bold?, italic?, color?}
  return new Paragraph({
    children: parts.map(p => new TextRun({
      text: p.text,
      bold: p.bold || false,
      italics: p.italic || false,
      color: p.color || undefined,
      size: 22,
    })),
    spacing: { before: 80, after: 80 },
  });
}

function highlight(text) {
  // Shaded info box via a bordered paragraph
  return new Paragraph({
    children: [new TextRun({ text, size: 22, color: "1a1a2e" })],
    shading: { type: ShadingType.CLEAR, fill: "EBF4FF" },
    border: {
      left: { style: BorderStyle.THICK, size: 12, color: "4A90D9" },
    },
    indent: { left: 300 },
    spacing: { before: 120, after: 120 },
  });
}

function tip(text) {
  return new Paragraph({
    children: [new TextRun({ text: "💡 " + text, size: 22, color: "1a3a1a" })],
    shading: { type: ShadingType.CLEAR, fill: "E8F5E9" },
    border: {
      left: { style: BorderStyle.THICK, size: 12, color: "4CAF50" },
    },
    indent: { left: 300 },
    spacing: { before: 120, after: 120 },
  });
}

function warn(text) {
  return new Paragraph({
    children: [new TextRun({ text: "⚠️  " + text, size: 22, color: "3a1a00" })],
    shading: { type: ShadingType.CLEAR, fill: "FFF8E1" },
    border: {
      left: { style: BorderStyle.THICK, size: 12, color: "FF9800" },
    },
    indent: { left: 300 },
    spacing: { before: 120, after: 120 },
  });
}

function codeBlock(text) {
  return new Paragraph({
    children: [new TextRun({ text, font: "Courier New", size: 20, color: "2d2d2d" })],
    shading: { type: ShadingType.CLEAR, fill: "F5F5F5" },
    border: {
      top: { style: BorderStyle.SINGLE, size: 4, color: "CCCCCC" },
      bottom: { style: BorderStyle.SINGLE, size: 4, color: "CCCCCC" },
      left: { style: BorderStyle.SINGLE, size: 4, color: "CCCCCC" },
      right: { style: BorderStyle.SINGLE, size: 4, color: "CCCCCC" },
    },
    indent: { left: 300, right: 300 },
    spacing: { before: 80, after: 80 },
  });
}

function spacer() {
  return new Paragraph({ text: "", spacing: { before: 60, after: 60 } });
}

function pageBreak() {
  return new Paragraph({
    children: [new TextRun({ break: 1 })],
  });
}

function makeTable(headers, rows, widths) {
  return new Table({
    width: { size: 100, type: WidthType.PERCENTAGE },
    rows: [
      new TableRow({
        tableHeader: true,
        children: headers.map((h, i) => new TableCell({
          children: [new Paragraph({
            children: [new TextRun({ text: h, bold: true, size: 20, color: "FFFFFF" })],
          })],
          width: widths ? { size: widths[i], type: WidthType.PERCENTAGE } : undefined,
          shading: { type: ShadingType.CLEAR, fill: "2E4057" },
        })),
      }),
      ...rows.map((row, ri) => new TableRow({
        children: row.map((cell, ci) => new TableCell({
          children: [new Paragraph({
            children: [new TextRun({ text: cell, size: 20 })],
          })],
          shading: { type: ShadingType.CLEAR, fill: ri % 2 === 0 ? "F8F9FA" : "FFFFFF" },
        })),
      })),
    ],
  });
}

